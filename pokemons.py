def main():
    global pokemon_level
    global day

    print("Welcome to Pokémon Evolution Simulator!")

    while True:
        print("----- MENU -----")
        print("1. Train Pokémon")
        print("2. Gym Battle")
        print("3. Display Pokémon Info")
        print("4. Exit")
        choice = input("Choose an option: ")
        if choice == "1":
            pokemon_level += 1

            messages = [
                "Your Pokémon practiced its moves!",
                "Your Pokémon ran laps to get stronger!",
                "Your Pokémon trained its reflexes!"
            ]
            print(random.choice(messages))

            evolve()

        elif choice == "2":
            print("Choose a battle type:")
            print("1. Normal Battle")
            print("2. Gym Battle")
            battle_choice = input("Enter your choice: ")

            if battle_choice == "1":
                print("Normal battle begins!")
                if random.choice(["win", "lose"]) == "win":
                    pokemon_level += 1
                    print("You won the normal battle! +1 level!")
                else:
                    print("You lost the normal battle...")

            elif battle_choice == "2":
                print("GYM BATTLE BEGINS!")
                if random.choice(["win", "lose"]) == "win":
                    pokemon_level += 3
                    print("You defeated the Gym Leader! +3 levels!")
                else:
                    print("You lost the Gym Battle!")

            else:
                print("Invalid battle choice!")

            evolve()

        elif choice == "3":
            info()
        elif choice == "4":
            print("Goodbye, trainer!")
            break

        else:
            print("Invalid option, try again!")


#Main
main()

